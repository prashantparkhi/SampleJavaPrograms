package javabasicsprogram;

public class Ternaryoperator {

	public static void main(String[] args) {
		
		int d=10;
		int m = 78;
		int min=(d<m)?d:m;
		int max = (d>m)?d:m;
		
		System.out.println("minimum number is: "+ min);
		System.out.println("maximum number is: "+ max);
		
	}
}
